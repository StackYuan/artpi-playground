size_t __write(int handle, const unsigned char *buffer, size_t size)
{
    if (buffer == NULL) {
        return 0;
    }

    // 只处理 stdout 和 stderr
    if (handle != _LLIO_STDOUT && handle != _LLIO_STDERR) {
        return _LLIO_ERROR;
    }

    // 通过 UART4 发送数据
    HAL_UART_Transmit(&huart4, (uint8_t *)buffer, size, HAL_MAX_DELAY);

    return size;
}
